# 멘토링_1(위클리페이퍼_2 추가)

## Q1. 회귀모델을 만들 때 이상치 제거가 필요할까요? 필요하다면 왜 그럴까요?
회귀모델에서 이상치를 제거하는 것은 필요할 수 있습니다. 
그 이유는 이상치가 모델의 성능을 저하시킬 수 있기 때문입니다. 
회귀모델은 기본적으로 데이터의 패턴을 추정하여 예측을 합니다. 
이상치는 매우 큰 값이나 매우 작은 값으로, 모델이 이를 반영하려고 할 때 회귀선이 왜곡될 수 있습니다. 
이로 인해 모델이 실제 데이터 패턴을 제대로 학습하지 못하고, 예측 결과에 부정적인 영향을 미칠 수 있습니다.

따라서 이상치를 제거하거나 조정함으로써 모델의 정확도를 높이고, 더 신뢰할 수 있는 결과를 얻을 수 있습니다. 
하지만, 모든 이상치가 반드시 제거되어야 하는 것은 아니므로, 도메인 지식에 기반하여 어떤 값이 이상치인지 신중하게 판단해야 합니다.

Q2. 데이터가 충분히 클 때 결측을 삭제하는 것에는 어떤 문제가 있을까요?
데이터가 충분히 크더라도 결측값을 삭제하는 것에는 문제가 있을 수 있습니다. 
그 이유는 다음과 같습니다:

정보 손실: 결측값이 포함된 데이터를 삭제하면 그 데이터를 통해 얻을 수 있는 정보도 함께 손실됩니다. 
특히, 결측값이 많을 경우 삭제된 데이터의 비율이 커져 분석의 신뢰성을 떨어뜨릴 수 있습니다.

대표성 문제: 삭제된 데이터가 특정 패턴을 가지고 있을 경우, 데이터를 삭제하는 것이 전체 데이터의 대표성을 왜곡할 수 있습니다. 
예를 들어, 결측값이 특정 그룹에 몰려 있다면 그 그룹의 특성이 분석에서 제외되므로 결과가 왜곡될 수 있습니다.

따라서 결측값을 단순히 삭제하기보다는 대체 방법을 고려하거나 결측값 패턴을 분석하여 처리 방법을 신중하게 선택해야 합니다.

Q3. 정규화가 왜 필요할까요? 정규화를 해서 얻는 부작용은 무엇이 있을까요?
정규화는 주로 특성들의 값 범위를 일정한 범위로 맞추기 위해 필요합니다. 그 이유는:

특성의 범위 차이 문제: 데이터에 여러 특성들이 있을 때, 각 특성의 값 범위가 매우 다를 수 있습니다. 
예를 들어, 키는 150~190 사이인데, 소득은 1000~5000 만원 사이일 수 있습니다. 
이러한 차이가 크면, 일부 특성이 모델 학습에 지나치게 영향을 미칠 수 있습니다. 
이를 방지하기 위해 정규화나 표준화가 필요합니다.

모델 성능 향상: 정규화를 통해 모든 특성의 범위가 비슷해지면, 모델이 각 특성에 대해 공정하게 가중치를 할당할 수 있게 됩니다. 
특히 경사하강법(Gradient Descent)을 사용하는 모델에서 수렴 속도가 빨라지고 성능이 향상됩니다.

하지만, 정규화에는 부작용도 존재할 수 있습니다:

정보 손실: 정규화 과정에서 데이터의 절대적인 크기나 분포 정보가 손실될 수 있습니다. 
예를 들어, 특정 특성이 원래 중요한 패턴을 나타내고 있었는데, 정규화를 통해 이 정보가 왜곡될 수 있습니다.
과도한 정규화: 데이터의 특성에 따라 정규화가 오히려 불필요할 수 있으며, 특히 이미 비슷한 범위의 특성들을 정규화하면 모델 성능에 큰 영향을 주지 않을 수 있습니다.
따라서 정규화가 꼭 필요한지, 그 방법이 적절한지 분석 후 진행하는 것이 중요합니다.

Q4. 데이터가 굉장히 많을 때 t-test를 하면 어떤 결과가 나올까요?
데이터가 굉장히 많을 때 t-test를 수행하면, 매우 작은 차이도 통계적으로 유의미하게 나올 수 있습니다. 
그 이유는 표본 크기가 커질수록 p값이 작아지기 때문입니다. 
즉, 데이터가 많으면 아주 작은 차이도 우연의 일치로 보기 어려워지므로, 유의미한 차이를 찾을 확률이 높아집니다.

하지만, 이는 실질적인 의미가 있을지에 대한 문제와는 별개입니다. 
예를 들어, 두 집단 간 평균 차이가 0.001만큼 차이 나도 데이터가 많으면 p값은 작게 나올 수 있지만, 이 차이가 실제로 의미 있는지에 대한 판단은 효과 크기(effect size) 등을 고려해야 합니다.

따라서 데이터가 많을 때는 p값만 보고 결과를 해석하기보다는, 차이의 실질적인 크기나 효과 크기를 함께 고려하는 것이 중요합니다.

Q5. 신장이 220cm인 데이터와 -1cm인 데이터 모두 IQR 규칙으로 평가하면 이상치로 나옵니다. 둘을 같은 방법으로 처리해야 할까요?
IQR 규칙(Interquartile Range)을 사용하여 이상치를 평가할 때, 220cm와 -1cm 모두 이상치로 판별될 수 있지만 이 둘을 같은 방법으로 처리하는 것은 적절하지 않을 수 있습니다.

도메인 지식 활용: 신장은 일반적으로 0cm 미만이나 300cm 이상일 수 없다는 도메인 지식이 있습니다. 
따라서 -1cm는 명백히 잘못된 값(예: 데이터 입력 오류)일 가능성이 높고, 이를 삭제하거나 수정하는 것이 좋습니다.
반면, 220cm는 매우 큰 값이지만 실제 신장 범위 내에서 허용될 수 있는 범위입니다. 
이 경우에는 이상치로 취급하지 않거나, 추가적으로 도메인 전문가의 검토를 받는 것이 좋습니다.

이상치 처리 방법: -1cm는 명백한 데이터 오류일 가능성이 크므로 수정하거나 삭제하는 것이 타당합니다. 
반면 220cm는 실제로 존재할 수 있는 신장 값일 수 있으므로, 이를 이상치로 처리하지 않거나, 다른 방법으로 검토할 필요가 있습니다.

따라서, IQR 규칙을 적용할 때는 도메인 지식을 기반으로 이상치 여부를 판단하는 것이 중요합니다.

요약
이상치 제거는 회귀모델 성능을 향상시키기 위해 필요할 수 있으며, 데이터 왜곡을 방지합니다.
결측값을 삭제하면 정보 손실이 발생하고, 데이터 대표성에 문제가 생길 수 있습니다.
정규화는 모델 성능을 개선하지만, 지나친 정규화는 정보 손실을 초래할 수 있습니다.
데이터가 많을 때 t-test는 작은 차이도 유의미하게 만들 수 있으므로, p값 외에도 효과 크기를 고려해야 합니다.
IQR 규칙은 신장과 같은 도메인 특성에 따라 다르게 처리해야 하므로, 도메인 지식이 중요합니다.
